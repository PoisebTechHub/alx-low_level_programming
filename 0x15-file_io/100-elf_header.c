#include <elf.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <fcntl.h>
#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>

/**
 * check_elf - confirms if a given file is an ELF file.
 * @e_ident: a pointer array with the ELF magic numbers.
 * Description: When file is not an ELF file - exit the at code 98.
 */
void check_elf(unsigned char *e_ident)
{
	int nest;

	for (nest = 0; nest < 4; nest++)
	{
		if (e_ident[nest] != 127 &&
		    e_ident[nest] != 'E' &&
		    e_ident[nest] != 'L' &&
		    e_ident[nest] != 'F')
		{
			dprintf(STDERR_FILENO, "Error: Not an ELF file\n");
			exit(98);
		}
	}
}

/**
 * print_magic - outputs the magic numbers of the ELF header.
 * @e_ident: a pointer array that contains the ELF magic numbers.
 * Description: magic numbers are separated by spaces.
 */
void print_magic(unsigned char *e_ident)
{
	int nest;

	printf("  Magic:   ");

	for (nest = 0; nest < EI_NIDENT; nest++)
	{
		printf("%02x", e_ident[nest]);

		if (nest == EI_NIDENT - 1)
			printf("\n");
		else
			printf(" ");
	}
}

/**
 * print_class - outputs the class of an ELF header.
 * @e_ident: a pointer array containing the ELF class.
 */
void print_class(unsigned char *e_ident)
{
	printf("  Class:                             ");

	switch (e_ident[EI_CLASS])
	{
	case ELFCLASSNONE:
		printf("none\n");
		break;
	case ELFCLASS32:
		printf("ELF32\n");
		break;
	case ELFCLASS64:
		printf("ELF64\n");
		break;
	default:
		printf("<unknown: %x>\n", e_ident[EI_CLASS]);
	}
}

/**
 * print_data - Outputs the data of an ELF header.
 * @e_ident: a pointer array containing the ELF class.
 */
void print_data(unsigned char *e_ident)
{
	printf("  Data:                              ");

	switch (e_ident[EI_DATA])
	{
	case ELFDATANONE:
		printf("none\n");
		break;
	case ELFDATA2LSB:
		printf("2's complement, little endian\n");
		break;
	case ELFDATA2MSB:
		printf("2's complement, big endian\n");
		break;
	default:
		printf("<unknown: %x>\n", e_ident[EI_CLASS]);
	}
}

/**
 * print_version - Outputs the version of an ELF header.
 * @e_ident: an array containing the ELF type.
 */
void print_version(unsigned char *e_ident)
{
	printf("  Version:                           %d",
	       e_ident[EI_VERSION]);

	switch (e_ident[EI_VERSION])
	{
	case EV_CURRENT:
		printf(" (current)\n");
		break;
	default:
		printf("\n");
		break;
	}
}

