#include <stdio.h>

/**
 * div - divide two integers
 * @a: first integer
 * @b: seond integer
 * Return: results of division
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * add - add two integers
 * @a: first integer
 * @b: seond integer
 * Return: sum of addition
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * mod - modulus of two integers
 * @a: first integer
 * @b: seond integer
 * Return: results of modulus
 */

int mod(int a, int b)
{
	return (a % b);
}


/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: seond integer
 * Return: final results
 */

int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiply two integers
 * @a: first integer
 * @b: seond integer
 * Returrn: results of multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}
